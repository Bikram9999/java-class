package day6;

public class OperatorDemo3 {

	public static void main(String[] args) {
		//4. Relational Operator > < >= <= == !=
		
		
		int a = 5;
		int b = 7;
		System.out.println(a > b);//false
		System.out.println(a < b);//true
		System.out.println(a >= b);//false
		System.out.println(a <= b);//true
		System.out.println(a != b);//true
		System.out.println(a == b);//false

	}

}
